/* 1文字 → 諧声データ */
const charDB = {};

function getXiesheng(char){

    const targets = charDB[char];

    if(!targets){
        return [];
    }

    const groups = [
        ...new Set(
            targets.map(x=>x.group)
        )
    ];

    const result = [];

    Object.values(xieshengDB).forEach(group=>{

        let currentGroup=null;

        group.forEach(row=>{

            if(row.group){
                currentGroup=row.group;
            }

            row.group=currentGroup;

            if(groups.includes(row.group)){

                const chars =
                    row.chars ||
                    [row.kanji];

                chars.forEach(k=>{

                    if(!result.some(
                        x=>
                        x.subgroup===row.subgroup &&
                        x.kanji===k
                    )){

                        result.push({
                            ...row,
                            kanji:k
                        });

                    }

                });

            }

        });

    });

    result.sort((a,b)=>{

        const na=
        Number(
            a.subgroup?.split("-")[1]
        )||0;

        const nb=
        Number(
            b.subgroup?.split("-")[1]
        )||0;

        return na-nb;

    });

    return result;

}

/* 自動で展開 */
Object.values(xieshengDB).forEach(group => {

    let currentGroup = null;

    group.forEach(row => {

        if(row.group){
            currentGroup = row.group;
        }

        row.group = currentGroup;

        const chars = row.chars || [row.kanji];

        chars.forEach(char => {

            if(!charDB[char]){
                charDB[char] = [];
            }

            charDB[char].push({
                ...row,
                kanji: char
            });

        });

    });

});

const list = getXiesheng("二");

/* subgroupごとにまとめる */
const grouped = {};

list.forEach(row => {

    const key = row.subgroup || row.kanji;

    if(!grouped[key]){
        grouped[key] = [];
    }

    const display = row.display || row.kanji;

    if(!grouped[key].includes(display)){
        grouped[key].push(display);
    }

});




